// Your task is to create a function that does four basic mathematical operations.
function fourBasicOperations(operation, value1, value2)
{
  switch(operation){
      case '+':
        return value1 + value2;
        break;
      case '-':
        return value1 - value2;
        break;
      case '*':
        return value1 * value2;
        break;
      case '/':
        return value1 / value2;
        break;
  }
 
}
